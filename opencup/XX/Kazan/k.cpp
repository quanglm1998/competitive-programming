int n, k;
int p[N];
vector<pair<int, int>> ls;
int res = 0;

struct DSU {
    int root[N];
    int numComp;
    vector<pair<int, int>> changed;

    DSU() {
        fill(root + 1, root + n + 1, -1);
        numComp = n;
    }

    int getRoot(int u) {
        while (root[u] > 0) u = root[u];
        return u;
    }

    bool unite(int u, int v) {
        u = getRoot(u);
        v = getRoot(v);
        if (u == v) return 0;
        if (root[u] > root[v]) swap(u, v);
        changed.push_back({u, root[u]});
        changed.push_back({v, root[v]});
        changed.push_back({0, 0});
        root[u] += root[v];
        root[v] = u;
        numComp--;
        return 1;
    }

    void reset(int u) {
        while (changed.size() > u) {
            auto val = changed.back();
            changed.pop_back();
            if (val.first) root[val.first] = val.second;
            else numComp++;
        }
    }
};

void solve(int u, int oddity, DSU &dsu) {
    if (u == ls.size()) {
        if (oddity) subTo(res, p[dsu.numComp]);
        else addTo(res, p[dsu.numComp]);
        return;
    }
    int numComp = dsu.numComp;
    int lastTime = dsu.changed.size();
    for (int i = 0; i < ls[u].first / 2; i++) {
        int x = ls[u].second + i;
        int y = ls[u].second + i + ls[u].first / 2;
        dsu.unite(x, y);
    }   
    if (numComp == dsu.numComp) return;
    solve(u + 1, oddity ^ 1, dsu);
    dsu.reset(lastTime);
    solve(u + 1, oddity, dsu);
}

