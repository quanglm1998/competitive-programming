template <int MOD>
class Modular {
  static_assert(MOD >= 0, "MOD can't be negative!");
  static_assert(MOD * 2 > 0, "MOD is too large for int");

 public:
  explicit Modular(long long v = 0) : v_((v %= MOD) < 0 ? v + MOD : v) {}

  Modular Inv() const { return Modular(Inverse(v_, MOD)); }
  Modular Power(long long v) const {
    Modular res(1), u(*this);
    for (; v; v >>= 1, u *= u)
      if (v & 1) res *= u;
    return res;
  }

  explicit operator int() const { return v_; }
  explicit operator bool() const { return static_cast<bool>(v_); }
  Modular& operator++() { return *this += Modular(1); }
  Modular& operator--() { return *this -= Modular(1); }
  Modular operator++(int) {
    Modular result(*this);
    *this += Modular(1);
    return result;
  }
  Modular operator--(int) {
    Modular result(*this);
    *this -= Modular(1);
    return result;
  }
  Modular operator-() const { return Modular(-v_); }
  Modular operator+() const { return Modular(*this); }
  Modular& operator+=(const Modular& other) {
    if ((v_ += other.v_) >= MOD) v_ -= MOD;
    return *this;
  }
  Modular& operator-=(const Modular& other) {
    if ((v_ -= other.v_) < 0) v_ += MOD;
    return *this;
  }
  Modular& operator*=(const Modular& other) {
    v_ = 1ll * v_ * other.v_ % MOD;
    return *this;
  }
  Modular& operator/=(const Modular& other) { return *this *= other.Inv(); }

  friend bool operator==(const Modular& lhs, const Modular& rhs) {
    return lhs.v_ == rhs.v_;
  }
  friend bool operator!=(const Modular& lhs, const Modular& rhs) {
    return !(lhs == rhs);
  }
  friend Modular operator+(const Modular& lhs, const Modular& rhs) {
    return Modular(lhs) += rhs;
  }
  friend Modular operator-(const Modular& lhs, const Modular& rhs) {
    return Modular(lhs) -= rhs;
  }
  friend Modular operator*(const Modular& lhs, const Modular& rhs) {
    return Modular(lhs) *= rhs;
  }
  friend Modular operator/(const Modular& lhs, const Modular& rhs) {
    return Modular(lhs) /= rhs;
  }
  friend std::istream& operator>>(std::istream& in, Modular& number) {
    long long v;
    in >> v;
    number = Modular(v);
    return in;
  }
  friend std::ostream& operator<<(std::ostream& out, const Modular& number) {
    return out << int(number);
  }
  friend string to_string(const Modular& number) {
    return to_string(int(number));
  }

 private:
  // return u : a * u == 1 (mod m)
  // NOTE: CAN'T assume that 0 <= u < m
  static int Inverse(int a, int m) {
    assert(a && "Divide by zero");
    auto u = 0, v = 1;
    while (a) {
      auto t = m / a;
      m -= t * a;
      swap(a, m);
      u -= t * v;
      swap(u, v);
    }
    assert(m == 1 && "Can't find inversion");
    return u;
  }

  int v_;
};
constexpr int MOD = (int)1e9 + 7;
using Mint = Modular<MOD>;

const int N = 300'010;

int n, m, k;
vector<int> adj[N];
Mint dp[N];
int lv[N];

// (min lv, res)
pair<int, Mint> Dfs(int u, int p) {
  lv[u] = lv[p] + 1;
  int back_edge = -1;
  int min_back_edge = n + 1;
  Mint res(1);
  for (auto v : adj[u]) {
    if (v == p) continue;
    if (lv[v]) {
      if (lv[v] > lv[u]) continue;
      assert(back_edge == -1);
      back_edge = lv[v];
    } else {
      auto foo = Dfs(v, u);
      min_back_edge = min(min_back_edge, foo.first);
      res *= foo.second;
    }
  }
  Mint mult = (back_edge == 1 || u == 1) == 1 ? Mint(k) : Mint(k - 1);
  if (back_edge != -1) {
    res *= dp[lv[u] - back_edge + 1] * mult;
    min_back_edge = min(min_back_edge, back_edge);
  }
  if (min_back_edge > lv[u]) {
    res *= mult;
  }
  // debug(u, min_back_edge, back_edge,res);
  return {min_back_edge, res};
}

void Solve(int test) {
  cin >> n >> m >> k;
  for (int i = 1; i <= n; i++) {
    adj[i].clear();
  }
  for (int i = 1; i <= m; i++) {
    int u, v;
    cin >> u >> v;
    adj[u].push_back(v);
    adj[v].push_back(u);
  }
  dp[1] = Mint(1);
  dp[2] = Mint(k - 1);
  Mint cur = Mint(k - 1);
  for (int i = 3; i <= n; i++) {
    cur *= Mint(k - 1);
    dp[i] = cur - dp[i - 1];
  }

  fill(lv + 1, lv + n + 1, 0);
  cout << Dfs(1, 0).second << '\n';
}

int32_t main() {
  ios_base::sync_with_stdio(0); cin.tie(0);
  int tc;
  cin >> tc;
  for (int test = 1; test <= tc; ++test) Solve(test);
  return 0;
}